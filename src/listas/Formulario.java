    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        int index = jComboBox1.getSelectedIndex();
        int length = Integer.parseInt(JOptionPane.showInputDialog("Ingresa el tama√±o de la lista"));
        LinkedList<Integer> Lista = integers.generarLista(length);
        jTextArea1.setText(Lista.toString());
        switch (index) {
            case 0 -> {
                int total = integers.SumarTodosLosElementos(Lista);
                jTextArea1.append("\nEl total de la suma de elementos es: " + total);
            }
            case 1 -> {
                int countOfNodes = integers.ContarNodos(Lista);
                jTextArea1.append("\nEl total de nodos en la lista es: " + countOfNodes);
            }
            case 2 -> {
                int addOfPrimes = integers.SumarTodosLosElementosPrimos(Lista);
                jTextArea1.append("\nEl total de la suma de elemento primos: " + addOfPrimes);
            }
            case 3 -> {
                int countOfPairNodes = integers.ContarNodosConValoresPares(Lista);
                jTextArea1.append("\nEl total de elementos pares es: " + countOfPairNodes);
            }
            case 4 -> {
                LinkedList<Integer> ListaB = integers.generarLista(length);
                jTextArea1.append("\n" + ListaB.toString());
                jTextArea1.append("\n" + integers.sumarDosListas(Lista, ListaB).toString());
            }
            case 5 -> {
                LinkedList<Integer> ordenado = new LinkedList<>();
                for (int i = 0; i < length; i++) {
                    int value = (int) (Math.random() * 50 + 1);
                    integers.insertInOrder(ordenado, value);
                }
                jTextArea1.setText(ordenado.toString());
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

